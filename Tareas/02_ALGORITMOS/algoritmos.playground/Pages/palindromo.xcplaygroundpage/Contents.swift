import UIKit

/**
 Algoritmo que nos dice si una cadena es o no un palindromo
 */

let texto : String = "dalad"
let texto_reversed : String = String(texto.reversed())

if(texto == texto_reversed){
    print("Tu cadena es un palindromo")
}else{
    print("Tu cadena no es un palindromo")
}



